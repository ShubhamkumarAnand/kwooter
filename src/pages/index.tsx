import { SignInButton, useUser } from "@clerk/nextjs";
import { type NextPage } from "next";
import Head from "next/head";

import { api } from "~/utils/api";
import type { RouterOutputs } from "~/utils/api";

import dayjs from "dayjs";
import relativeTime from "dayjs/plugin/relativeTime";
import Image from "next/image";
dayjs.extend(relativeTime);

const CreateWizard = () => {
	const { user } = useUser();
	if (!user) return null;
	return (
		<div className="flex w-full gap-3">
			<Image
				src={user.profileImageUrl}
				alt="profile image"
				className="rounded-full"
				width={56}
				height={56}
			/>
			<input
				type="text"
				placeholder="Type some Text"
				className="bg-transparent grow outline-none text-white"
			/>
		</div>
	);
};

type PostWithUser = RouterOutputs["post"]["getAll"][number];

const PostView = (props: PostWithUser) => {
	const { post, author } = props;
	return (
		<div className="p-4 border-b border-white flex gap-3" key={post.id}>
			<Image
				src={author.profileImageUrl}
				alt="profile Image"
				className="rounded-full"
				width={48}
				height={48}
			/>
			<div className="flex flex-col">
				<div className="text-slate-300 font-semibold">
					<span>{`@${author.username}`} </span>
					<span className="font-light ">{` . ${dayjs(
						post.createdAt,
					).fromNow()}`}</span>
				</div>
				<span>{post.content}</span>
			</div>
		</div>
	);
};

const Home: NextPage = () => {
	const user = useUser();
	const { data, isLoading } = api.post.getAll.useQuery();

	if (isLoading) {
		return <div>is Loading..</div>;
	}
	if (!data) {
		return <div>Something went wrong</div>;
	}

	return (
		<>
			<Head>
				<title>Kwootter</title>
				<meta name="description" content="Generated by create-t3-app" />
				<link rel="icon" href="/favicon.ico" />
			</Head>
			<main className="flex h-screen justify-center bg-gradient-to-b from-[#091e20] to-[#051214]">
				<div className="h-full w-full md:max-w-2xl border-slate-400 border-x">
					<div className="flex p-4 border-b border-slate-400">
						<div>
							{!user.isSignedIn && (
								<div className="flex justify-center text-white border border-white p-2 rounded">
									<SignInButton />
								</div>
							)}
						</div>
						{user.isSignedIn && <CreateWizard />}
					</div>
					<div className="flex flex-col align-middle">
						{[...data, ...data]?.map((fullPost) => (
							<PostView {...fullPost} key={fullPost.post.id} />
						))}
					</div>
				</div>
			</main>
		</>
	);
};

export default Home;
